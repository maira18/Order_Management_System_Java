    public boolean checkTextFields()
    {
        String name=jTextField7_name4.getText();
        email=jTextField1_email4.getText();
        String pno=jTextField4_Pno4.getText();
        String add=jTextField5_add4.getText();
        String pass=String.valueOf(jPasswordField1_pass4.getPassword());
        String conpass=String.valueOf(jPasswordField2_conpass4.getPassword());
        
        if(name.trim().equals("") || email.trim().equals("") || pno.trim().equals("") || add.trim().equals("") ||
                pass.trim().equals("") || conpass.trim().equals(""))
        {
            JOptionPane.showMessageDialog(null, "One or more fields are EMPTY", "Empty Fields", 3);
            return false;
        }
        else if(!(pass.equals(conpass)))
        {
            JOptionPane.showMessageDialog(null, "Password doesn't match", "Unmatched Password", 3);
            return false;
        }
        else{return true;}
    }

   public boolean checkDuplicateEmail()
   {
       PreparedStatement st;
       ResultSet rs;
       email=jTextField1_email4.getText();
       String query="SELECT * FROM customer WHERE email=?";
       try {
            st = db_connection.getConnection().prepareStatement(query);
            st.setString(1, email);
            rs=st.executeQuery();
            if(rs.next())
            {
                JOptionPane.showMessageDialog(null, "Email already Registered", "Duplicate Email", 3);
                return true;
            }
        } 
       catch (SQLException ex) {}
       return false;
   }
    
