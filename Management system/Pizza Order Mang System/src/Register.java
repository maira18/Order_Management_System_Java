    private void jButton1_registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1_registerActionPerformed

        String name=jTextField7_name4.getText();
        email=jTextField1_email4.getText();
        String pno=jTextField4_Pno4.getText();
        String add=jTextField5_add4.getText();
        String pass=String.valueOf(jPasswordField1_pass4.getPassword());
        String conpass=String.valueOf(jPasswordField2_conpass4.getPassword());
        if(checkTextFields() && (!checkDuplicateEmail()))
        {
            PreparedStatement st;
            ResultSet rs;
            String query="INSERT INTO `customer`(`name`, `email`, `password`, `address`, `number`) VALUES (?,?,?,?,?)";
            try {
            st = db_connection.getConnection().prepareStatement(query);
            st.setString(1, name);
            st.setString(2, email);
            st.setString(3, pass);
            st.setString(4, add);
            st.setString(5, pno);
            
            if(st.executeUpdate()>0)
            {
                JOptionPane.showMessageDialog(null, "Account Created successfully");
                Menu m=new Menu();
                m.setVisible(true);
                m.setLocationRelativeTo(null);
                this.setVisible(false);
            }
            else{
                JOptionPane.showMessageDialog(null, "Error in Creating Account");
            }
        } 
       catch (SQLException ex) {}
        }
        
    }//GEN-LAST:event_jButton1_registerActionPerformed

